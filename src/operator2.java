
public class operator2 {

	public static void main(String[] args) {
		int i = 10;
		int j = 10;
		
		System.out.println(i == j); // i = j boolean 값 출력
		System.out.println(i <= j); // i ≤ j boolean 값 출력
		System.out.println(i != j); // i = j boolean 값 반대로 출력
		
		i += 10; // i = i + 10;
		
		System.out.println(i);
		
		System.out.println(i -= 10);
		System.out.println(i);
	}

}
